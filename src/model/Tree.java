package model;

public class Tree {

	private Accessory rootAccesory;
	private Expense rootExpense;
	
	
	public Tree() {
		super();
		this.rootAccesory = null;
		this.rootExpense = null;
	}
	
	public Accessory getRootAccesory() {
		return rootAccesory;
	}
	public void setRootAccesory(Accessory rootAccesory) {
		this.rootAccesory = rootAccesory;
	}
	public Expense getRootExpense() {
		return rootExpense;
	}
	public void setRootExpense(Expense rootExpense) {
		this.rootExpense = rootExpense;
	}

	

} //end of class
